package Day05_matematikselişlemler;

public class C01_PreincrementPostincrement {
    public static void main(String[] args) {

        int sayi=10;
        System.out.println(sayi);
        sayi++;
        System.out.println(sayi);
        System.out.println(++sayi);
        System.out.println(sayi);
        // artırp yazdırma- yazdırıp artırma

        System.out.println(sayi++);
        System.out.println(sayi);








    }
}
